package primitive;

public class IntExample {
	public static void main(String[] args) {
		int var1 = 0b1010; // 2진수
		int var2 = 012; // 8진수
		int var3 = 0xA; // 16진수
		// 0, 1, 2, 3, ..., 9, A, B, C, ,D, E, F
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		
	}

}
