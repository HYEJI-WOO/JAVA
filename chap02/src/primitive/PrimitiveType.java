package primitive;

public class PrimitiveType {
	public static void main(String[] args) {
		// 정수 타입
		byte byteValue = 100;
		char chValue = 'F';
		short shValue = 100;
		int intValue = 124;
		long longValue = 1535;
		
		// 실수 타입 
		float floutV1 = 10.111f;
		float floutV2 = 3.14F; // 접미사 f 생략 불가능
		double doubleV1 = 3.141592D;
		double doubleV2 = 3.141592d;
		double doubleV3 = 3.141592; // 접미사 d 생략 가능 
		
		// 논리 타입
		boolean boolV1 = true;
		boolean boolV2 = false;
	}
}
